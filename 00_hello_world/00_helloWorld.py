# Tipos de datos y Estructuras de datos en Python
print(type(1)) #int 
print(type(1.2)) #float
print(type("Hola Mundo")) #str
print(type(True)) #bool
print(type(3 + 3j)) #complex
print(type(["Pepe", "Guillermo", 1])) # list
print(type({"Nombre": "Valentino", "Apellido": "Paterno", "Edad": 20})) #dictionary
print(type(("Pepe", "Sandra", "Kira"))) # tuple
print(type({1, 3, 2, 5})) #set